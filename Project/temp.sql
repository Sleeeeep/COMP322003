DROP DATABASE IF EXISTS PROJECT;
CREATE DATABASE PROJECT DEFAULT CHARACTER SET utf8 COLLATE utf8_general_ci;
USE PROJECT;

DROP TABLE IF EXISTS CUSTOMER;
CREATE TABLE CUSTOMER(
	Id VARCHAR(20) NOT NULL,
	Pw VARCHAR(15) NOT NULL,
	Name VARCHAR(5) NOT NULL,
	Sex VARCHAR(2),
	Job VARCHAR(20),
	Type VARCHAR(5),
	Bdate DATE,
	Address VARCHAR(50),
	Pnumber CHAR(13) NOT NULL,
	PRIMARY KEY(ID)
);

DROP TABLE IF EXISTS CATEGORY;
CREATE TABLE CATEGORY(
	Large VARCHAR(10) NOT NULL,
	Middle VARCHAR(10) NOT NULL,
	Small VARCHAR(10) NOT NULL,
	PRIMARY KEY(Small)
);

DROP TABLE IF EXISTS BRAND;
CREATE TABLE BRAND(
	Bnumber INT NOT NULL,
	Bname VARCHAR(10) NOT NULL,
	UNIQUE(Bname),
	PRIMARY KEY(Bnumber)
);

DROP TABLE IF EXISTS ITEM;
CREATE TABLE ITEM(
	Code INT NOT NULL,
	Name VARCHAR(20) NOT NULL,
	Measure VARCHAR(10),
	Price INT,
	Stock INT,
	Category VARCHAR(10),
	PRIMARY KEY(Code),
	FOREIGN KEY(Category) REFERENCES CATEGORY(Small) ON UPDATE CASCADE ON DELETE RESTRICT
);

DROP TABLE IF EXISTS BRAND_ITEM;
CREATE TABLE BRAND_ITEM(
	Bno INT NOT NULL,
	Item INT NOT NULL,
	PRIMARY KEY(Bno, Item),
	FOREIGN KEY(Bno) REFERENCES BRAND(Bnumber) ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(Item) REFERENCES ITEM(Code) ON UPDATE CASCADE ON DELETE CASCADE
);
	
DROP TABLE IF EXISTS SUPPLIER;
CREATE TABLE SUPPLIER(
	Snumber INT NOT NULL,
	Sname VARCHAR(10) NOT NULL,
	UNIQUE(Sname),
	PRIMARY KEY(Snumber)
);

DROP TABLE IF EXISTS SUPPLY_BRAND;
CREATE TABLE SUPPLY_BRAND(
	Sno INT NOT NULL,
	Bno INT NOT NULL,
	PRIMARY KEY(Sno, Bno),
	FOREIGN KEY(Bno) REFERENCES BRAND(Bnumber) ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(Sno) REFERENCES SUPPLIER(Snumber) ON UPDATE CASCADE ON DELETE CASCADE
);

DROP TABLE IF EXISTS SHIPPINGCOMPANY;
CREATE TABLE SHIPPINGCOMPANY(
	SCnumber INT NOT NULL,
	SCname VARCHAR(10) NOT NULL,
	UNIQUE(SCname),
	PRIMARY KEY(SCnumber)
);

DROP TABLE IF EXISTS ORDERS;
CREATE TABLE ORDERS(
	Onumber INT NOT NULL,
	Cid VARCHAR(20) NOT NULL,
	SCno INT,
	Stime DATE NOT NULL,
	Ctime DATE,
	PRIMARY KEY(Onumber),
	FOREIGN KEY(Cid) REFERENCES CUSTOMER(Id) ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(SCno) REFERENCES SHIPPINGCOMPANY(SCnumber) ON UPDATE CASCADE ON DELETE CASCADE
);

DROP TABLE IF EXISTS SHOPPINGCART;
CREATE TABLE SHOPPINGCART(
	Cid VARCHAR(20) NOT NULL,
	Item INT,
	FOREIGN KEY(Item) REFERENCES ITEM(Code) ON UPDATE CASCADE ON DELETE CASCADE
);

DROP TABLE IF EXISTS ORDER_LIST;
CREATE TABLE ORDER_LIST(
	Ono INT NOT NULL,
	Item INT NOT NULL,
	FOREIGN KEY(Ono) REFERENCES ORDERS(Onumber) ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(Item) REFERENCES ITEM(Code) ON UPDATE CASCADE ON DELETE RESTRICT
);

DROP TABLE IF EXISTS SHIPPING_REGION;
CREATE TABLE SHIPPING_REGION(
	SCno INT NOT NULL,
	Region VARCHAR(10) NOT NULL,
	PRIMARY KEY(SCno, Region),
	FOREIGN KEY(SCno) REFERENCES SHIPPINGCOMPANY(SCnumber) ON UPDATE CASCADE ON DELETE CASCADE
);

